package com.syntax.class10;

public class Homework {
	public static void main(String[] args) {

		for (int r = 5; r >= 1; r--) {
			for (int c = 1; c <= r; c++) {
				System.out.print(r);
			}
			System.out.println();
		}

		for (int i = 5; i >= 1; i--) {
			for (int j = i; j >= 1; j--) {
				System.out.print(j);
			}
			System.out.println();
		}

	}

}
