package com.syntax.class13;

public class Dog {
	
	String breed;
	String color;
	String size;
	
	void bark() {
		System.out.println(size + " " + color + " " + breed + " barks");
	}
	
	void nap() {
		System.out.println(size + " " + color + " " + breed + " naps");
	}
	
	void cuddle() {
		System.out.println(size + " " + color + " " + breed + " cuddles");
	}

}
